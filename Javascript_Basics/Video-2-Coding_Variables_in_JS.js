// How to create variables in JS

// there are 3 ways to create variables in JS

// first way - var keyword
// Syntax
// var <variable name> = <value>;
var marks = 90;
var score = 5;

// second way - let keyword
// let <variable name> = <value>;
let age = 24;
let flag = 0;

// third way - const keyword
// const <variable name> = <value>;
const number = 10;
const x = 900;


// Semicolon is optional in JS 
// It is not mandatory but in some languages it is mandatory like C++,JAVA 
// semicolon is like full stop in english language 
