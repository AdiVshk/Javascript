// Logical Operators

// AND gate take two inputs
// In AND gate if one value is False everything is false

// OR gate take two inputs
// In OR gate if one value is True everything is True

// NOT gate takes only one input
// X is true  --> false
// Y is false --> true

// If you want to write Logical AND in JS
// &&

// If you want to write Logical OR in JS
// ||

// If you want to write Logical NOT in JS
// !value

console.log(true && false); // false
console.log(false || true); // true
console.log(!false); // true
console.log(10 > 5 && 900 < 100); // false
