

// Operators 

// There are different types of Operators 


// Arithmetic Operators 
// + , - , / , * , % 

// Where do you apply operators ? 
// On operands- values which we want to do operation 

let a = 30;
let b = 40;

// 30 and 40 --> are operands 
// + --> is operator

console.log(a+b); // addition 
console.log(a-b); // subtraction 
console.log(a*b); // multiplication
console.log(a/b); // division --> Quotient
console.log(a%b); // division --> remainder 
console.log(a**3); // exponent 


// Assignment Operators 

// = , += , -= , /= , %= 


let x = 10;
// = --> assignment operator
// just directly assigns the value from RHS to LHS 

// x+=2 --> x=x+2 
// x-=2 --> x=x-2
// x/=2 --> x=x+2
// x%=2 --> x=x%2 

console.log(x+=2);
console.log(x-=2);
console.log(x/=2);
console.log(x%=2);